#include "cube3d.h"

void	rotate(t_draw *dir)
{
	dir->cam.d_x = dir->cam.d_x * cos(0.05) - dir->cam.d_y * sin(0.05);
	dir->cam.d_y = dir->cam.d_x * sin(0.05) + dir->cam.d_y * cos(0.05);
}
